     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'category' => 'required',
            'title' => 'required',
        ]);


        $port = new Portfolio([
            'category' => $request->get('category'),
            'title' => $request->get('title'),

        ]);
        $port->save();
        $portList = Portfolio::all();
        
        return view("admin.portfolio.index",compact('portList'));
    }

